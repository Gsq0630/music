package org.lanqiao.entity;

public class UserSongList {
    private Integer uslId;

    private Integer songlistId;

    private Integer userId;

    private Integer usRelation;

    public Integer getUslId() {
        return uslId;
    }

    public void setUslId(Integer uslId) {
        this.uslId = uslId;
    }

    public Integer getSonglistId() {
        return songlistId;
    }

    public void setSonglistId(Integer songlistId) {
        this.songlistId = songlistId;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public Integer getUsRelation() {
        return usRelation;
    }

    public void setUsRelation(Integer usRelation) {
        this.usRelation = usRelation;
    }
}